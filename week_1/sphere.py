import math


radian = float(input("Enter the Radius of Sphere :"))
sur_area = 4 * math.pi * (radian **2)
volume = (4/3) * math.pi * (radian ** 3)
print("Surface Area of the Sphere : ", sur_area)
print("Volume of the Sphere : ", volume)
